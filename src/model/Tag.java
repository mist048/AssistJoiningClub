package last;

public class Tag {
	private String id;
	private String name;
	Tag(){
		
	}
	
	protected String getId() {
		return id;
	}
	
	protected void setId(String id) {
		this.id = id;
	}
	
	protected String getName() {
		return name;
	}
	
	protected void setName(String name) {
		this.name = name;
	}

}
